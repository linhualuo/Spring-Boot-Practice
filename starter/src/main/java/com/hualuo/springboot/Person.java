package com.hualuo.springboot;

/**
 * Bean class Person
 *
 * @author Joseph
 * @create 2017/9/14 12:30
 */
public class Person {

    private String name;

    private int age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
